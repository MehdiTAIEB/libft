int		ft_strcmp(const char *s1, const char *s2)
{
	int	u;

	u = 0;
	while (s1[u] || s2[u])
	{
		if (s1[u] != s2[u])
			return ((unsigned char)s1[u] - (unsigned char)s2[u]);
		u++;
	}
	return (0);
}
